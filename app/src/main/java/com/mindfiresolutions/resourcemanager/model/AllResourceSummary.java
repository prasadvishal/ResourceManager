    public void setViewSoftwareSummary(List<ViewSoftwareSummary> mViewSoftwareSummary) {
        this.mViewSoftwareSummary = mViewSoftwareSummary;
    }

    public List<ViewSharedResource> getViewSharedResource() {
        return mViewSharedResource;
    }

    public void setViewSharedResource(List<ViewSharedResource> mViewSharedResource) {
        this.mViewSharedResource = mViewSharedResource;
    }

