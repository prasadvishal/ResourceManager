package com.mindfiresolutions.resourcemanager.model;

import com.google.gson.annotations.SerializedName;

import java.util.List;

/**
 * pojo to get list of hardwares by sending types id
 * Created by Vishal Prasad on 5/1/2017.
 */

public class HardwareByTypeResponse {
    @SerializedName("Response")
    private ResponseGetterBase mResponse;
    @SerializedName("HardwareDetails")
    private List<HardwareDetails> mHardwareDetails = null;

    public ResponseGetterBase getResponse() {
        return mResponse;
    }

    public void setResponse(ResponseGetterBase mResponse) {
        this.mResponse = mResponse;
    }

    public List<HardwareDetails> getHardwareDetails() {
        return mHardwareDetails;
    }

    public void setHardwareDetails(List<HardwareDetails> mHardwareDetails) {
        this.mHardwareDetails = mHardwareDetails;
    }

}
