package com.mindfiresolutions.resourcemanager.model;


import com.google.gson.annotations.SerializedName;

/**
 * Created by Shivangi Singh on 4/5/2017.
 */

public class HardwareSummaryList {

    @SerializedName("Type")
    private Integer type;
    @SerializedName("HardwareType")
    private String hardwareType;
    @SerializedName("Status")
    private Integer status;
    @SerializedName("HardwareStatus")
    private String hardwareStatus;

    public Integer getType() {
        return type;
    }

    public void setType(Integer type) {
        this.type = type;
    }

    public String getHardwareType() {
        return hardwareType;
    }

    public void setHardwareType(String hardwareType) {
        this.hardwareType = hardwareType;
    }

    public Integer getStatus() {
        return status;
    }

    public void setStatus(Integer status) {
        this.status = status;
    }

    public String getHardwareStatus() {
        return hardwareStatus;
    }

    public void setHardwareStatus(String hardwareStatus) {
        this.hardwareStatus = hardwareStatus;
    }

}
