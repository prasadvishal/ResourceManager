package com.mindfiresolutions.resourcemanager.model;

import com.google.gson.annotations.SerializedName;

/**
 * Created by Vishal Prasad on 5/10/2017.
 */

public class SharedResourcesCount {
    @SerializedName("TotalSharedResources")

    private Integer totalSharedResources;
    @SerializedName("AvailableSharedResources")

    private Integer availableSharedResources;
    @SerializedName("AssignedSharedResources")

    private Integer assignedSharedResources;

    public Integer getTotalSharedResources() {
        return totalSharedResources;
    }

    public void setTotalSharedResources(Integer totalSharedResources) {
        this.totalSharedResources = totalSharedResources;
    }

    public Integer getAvailableSharedResources() {
        return availableSharedResources;
    }

    public void setAvailableSharedResources(Integer availableSharedResources) {
        this.availableSharedResources = availableSharedResources;
    }

    public Integer getAssignedSharedResources() {
        return assignedSharedResources;
    }

    public void setAssignedSharedResources(Integer assignedSharedResources) {
        this.assignedSharedResources = assignedSharedResources;
    }

}