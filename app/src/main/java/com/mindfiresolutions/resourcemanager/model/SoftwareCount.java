package com.mindfiresolutions.resourcemanager.model;

import com.google.gson.annotations.SerializedName;

/**
 * Created by Vishal Prasad on 5/10/2017.
 */

public class SoftwareCount {
    @SerializedName("TotalSoftware")

    private Integer totalSoftware;
    @SerializedName("AvailableSoftware")

    private Integer availableSoftware;
    @SerializedName("AsssignedSoftware")

    private Integer asssignedSoftware;

    public Integer getTotalSoftware() {
        return totalSoftware;
    }

    public void setTotalSoftware(Integer totalSoftware) {
        this.totalSoftware = totalSoftware;
    }

    public Integer getAvailableSoftware() {
        return availableSoftware;
    }

    public void setAvailableSoftware(Integer availableSoftware) {
        this.availableSoftware = availableSoftware;
    }

    public Integer getAsssignedSoftware() {
        return asssignedSoftware;
    }

    public void setAsssignedSoftware(Integer asssignedSoftware) {
        this.asssignedSoftware = asssignedSoftware;
    }

}

