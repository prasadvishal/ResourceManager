package com.mindfiresolutions.resourcemanager.model;

import com.google.gson.annotations.SerializedName;

/**
 * Created by Shivangi Singh on 6/6/2017.
 */

public class SoftwareKeyList {
        @SerializedName("SoftwareKeyID")
        private Integer softwareKeyID;
        @SerializedName("TotalKeys")
        private Integer totalKeys;
        @SerializedName("AvailableKeys")
        private Integer availableKeys;
        @SerializedName("LicenseKeyValue")
        private String licenseKeyValue;
        @SerializedName("PurchasedDate")
        private String purchasedDate;
        @SerializedName("Purchased_By")
        private String purchasedBy;
        @SerializedName("ValidUpto")
        private String validUpto;

        public Integer getSoftwareKeyID() {
            return softwareKeyID;
        }

        public void setSoftwareKeyID(Integer softwareKeyID) {
            this.softwareKeyID = softwareKeyID;
        }

        public Integer getTotalKeys() {
            return totalKeys;
        }

        public void setTotalKeys(Integer totalKeys) {
            this.totalKeys = totalKeys;
        }

        public Integer getAvailableKeys() {
            return availableKeys;
        }

        public void setAvailableKeys(Integer availableKeys) {
            this.availableKeys = availableKeys;
        }

        public String getLicenseKeyValue() {
            return licenseKeyValue;
        }

        public void setLicenseKeyValue(String licenseKeyValue) {
            this.licenseKeyValue = licenseKeyValue;
        }

        public String getPurchasedDate() {
            return purchasedDate;
        }

        public void setPurchasedDate(String purchasedDate) {
            this.purchasedDate = purchasedDate;
        }

        public String getPurchasedBy() {
            return purchasedBy;
        }

        public void setPurchasedBy(String purchasedBy) {
            this.purchasedBy = purchasedBy;
        }

        public String getValidUpto() {
            return validUpto;
        }

        public void setValidUpto(String validUpto) {
            this.validUpto = validUpto;
        }
    }

