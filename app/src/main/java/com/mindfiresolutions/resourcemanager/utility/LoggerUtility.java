package com.mindfiresolutions.resourcemanager.utility;

import android.content.Context;
import android.util.Log;
import android.widget.Toast;

/*
 * this utility has log and toast utilities to be used
 * Created by Shivangi Singh on 3/15/2017
 * modified on 5/31/2017
 */
public class LoggerUtility {
    //utility for short toast
    public static void toastShort(Context context, String msg) {
        Toast.makeText(context, msg, Toast.LENGTH_SHORT).show();
    }

    //utility for long toast
    public static void toastLong(Context context, String msg) {

        Toast.makeText(context, msg, Toast.LENGTH_LONG).show();
    }

    //utility for log
    public static void log(String tag, String msg) {
        Log.e(tag, String.valueOf(msg));
    }
}



